
public class Contact {
	private String name;
	private int age;
	private long number;
	
	public Contact(String name, int age, long number) {
		this.name = name;
		this.age = age;
		this.number = number;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public long getNumber() {
		return number;
	}
	
	//getters//
	
	

}
